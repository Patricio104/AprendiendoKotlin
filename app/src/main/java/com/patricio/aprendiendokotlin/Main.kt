package com.patricio.aprendiendokotlin


fun main(){

    //when equivalente a switch de java
    val x =  4
    when(x){
        1 ->{
            println("X es 1")
        }
        2 ->{
            println("X es 2")
        }
        3 ->{
            println("X es 3")
        }
        else ->{
            println("X no corresponde a ninguna funcion declarada")
        }
    }
}



